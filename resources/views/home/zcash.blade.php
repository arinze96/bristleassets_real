                        <p>
                            Cryptocurrencies have gone from just one type in 2009 (Bitcoin) to hundreds, and it seems
                            like more show up with every passing day. One of these recent additions is Zcash, which
                            landed in 2016 and caught the attention of the market very quickly. Trading pushed it to an
                            unprecedented $4,000 U.S. per coin, but what was behind this huge flurry of interest? <br>
                            <br>

                            Probably the fact that Zcash CEO Zooko Wilcox-O’Hearn spent three years working alongside
                            top cryptographers on comprehensive research, because they were very interested in finding a
                            way to make a new cryptocurrency that solved the privacy concerns that other coins have been
                            dogged by. <br> <br>

                            Strings of letters and numbers that identify each user are the basis for the private keys
                            which underpin the majority of cryptocurrency transactions, but due to the nature of
                            blockchain these public records make it possible to examine the buying habits of users, so
                            doing a little digging means that you can potentially find out their identities. <br> <br>

                            Zcash uses a zero-knowledge proof concept called "zk-SNARKs," that lets users swap
                            transactional info with each other without also handing over such identifying details. Zcash
                            transactions appear on the public blockchain too, but it lets users apply a feature that
                            makes details of the sender, the recipient, and the transaction amount stay private. So,
                            it’s no wonder that Zcash has created so much interest, and that so many new users want to
                            get involved with crypto currency mining. Let’s take a look at how to get started.
                        </p>

                        <h3>
                            Zcash Mining: How Does It Work?
                        </h3>

                        <p>
                            Like every cryptocurrency, Zcash coins are created by mining. They’re called ZECs and no one
                            comes into being each time somebody adds a block to the Zcash blockchain, which is something
                            that happens every 2½ minutes. <br> <br>

                            Zcash’s launch was different from many of occurrences because it had a “slow-start
                            mechanism," which means that fewer coins were released into the system. 20,000 were mined in
                            the first month, which is fewer than most, but this deliberate ploy was an insurance policy.
                            It meant that if there were any teething troubles or security vulnerabilities fewer coins
                            would be affected. <br> <br>

                            All mining of cryptocurrencies uses a "proof of work" algorithm and the one for Zcash is
                            called "Equihash." It runs on typical off-the-shelf computer hardware which is a real
                            blessing for those new to mining. Some cryptocurrencies require expensive and powerful ASIC
                            systems, but Equihash can run on a home set up just as effectively. Most people have a
                            laptop or PC in the house and they would probably prefer using that to spending thousands on
                            an ASIC unit just to get started. <br> <br>

                            People can just use their own computer to join a Zcash cloud mining group and start earning
                            quite quickly.
                        </p>

                        <h3>
                            Selecting the Right Hardware For Zcash Mining
                        </h3>

                        <p>
                            Although we’ve said that you can start with really have, it’s still the case that more
                            powerful computers can crunch more numbers which means that they can help you make more
                            profits. Equihash like to use a lot of RAM, and the more powerful your CPUs and GPUs, the
                            better. You’ll also want to invest in good cooling because mining Zcash produces a lot of
                            heat. Water cooling is very effective and off-the-shelf systems are available at reasonable
                            cost. <br> <br>

                            Manufacturers like Nvidia have been upping their game in the video card was since the dawn
                            of computing history, in the latest cards are incredibly fast, with thousands of processing
                            cores. Graphics processing units, or GPUs our ideal for mining, and is now possible to buy
                            graphics cards that have been adapted to handle the unique tasks of mining more efficiently.
                            As we said, the graphics card that you have in your current setup will probably be adequate,
                            but if you want to invest in more horsepower your mining set up then it’s going to pay you
                            back more quickly.
                        </p>

                        <h3>
                            ZCash Mining Reward - How Much Can You Earn?
                        </h3>

                        <p>
                            Miners want to receive a reward, and ideally, as quickly as possible. As a result, many
                            wonder how much you can earn with Zcash. The Zcash monetary base is the same as bitcoin,
                            which is a total amount of coins produced of 21 million ZEC units. However, 10 percent of
                            that reward is distributed to the stakeholders in the Zcash company, which includes the
                            founders, investors, advisors and employees. Zcash calls this the "Founders Reward". <br>
                            <b></b>

                            For the first four years of operation, 50 ZEC will be created every 10 minutes, and 80
                            percent of that newly created ZEC goes to the miners while 20 percent goes to the founders.
                            Every four years, the rate of ZEC being created will halve, similar to Bitcoin. <br> <br>

                            So how much will you earn? Currently, the reward is set at 12.5 ZEC per block. However, each
                            four-year period (or 840,000 blocks mined) that reward is cut in half. For example, in the
                            year 2020 the reward is expected to drop to 6.25 and in the year 2024 the reward is
                            forecasted to drop to 3.12 and so on (until the currency reaches its cap). This will make
                            mining more difficult over time. As with most new cryptocurrencies, those who start mining
                            early may have an advantage when it comes to earning rewards.
                        </p>

                        <h3>
                            Determining ZCash Mining Profitability
                        </h3>

                        <p>
                            We discussed the Zcash mining reward, but let’s put things in perspective and explore the
                            potential reward. How much can you earn mining? <br> <br>

                            The amount of your reward will depend on a variety of factors, including the equipment
                            purchased, electricity cost in your area, and whether you belong to a mining pool or mine
                            alone. But regardless, you can figure out your unique situation by using a profit
                            calculator. <br> <br>

                            For example, this calculator allows you to input the hashing power, power consumption and
                            cost per kW/h to determine your profit ratio per day and per month. Prior to purchasing
                            equipment, you can use this type of calculator to determine whether the initial investment
                            is worth the potential profit. <br> <br>

                            For example, you might already have a CPU, but determine that investing in a GPU allows you
                            to significantly increase your profit potential. As a result, that upfront investment is
                            worth the cost.
                        </p>

                        <h3>
                            Setting Up a Zcash Wallet Address
                        </h3>

                        <p>
                            Once you start earning ZEC coins, it’s critical to have a safe place to store your funds.
                            This is basically a Zcash wallet address, and there are a few different options available,
                            including: <br> <br>

                            Internet wallets. Internet wallets have apps that you can download to your smartphone to
                            view and manage your wallet. Here is a guide to help you compare before selecting an option.
                            <br> <br>

                            Hardware wallets. According to Zcash, a hardware wallet is the safest method for storing
                            your Zcash. The drawback is that they currently support only Zcash transparent addresses,
                            and those costs ranges from $40 to $99. <br> <br>

                            Exchange wallets. The exchange wallets are among the easiest options to set up. You simply
                            need to create an account with an exchange that supports Zcash. However, check out the
                            security of the exchange when considering this option. <br> <br>

                            The decision of which wallet to select is key when storing your funds because with
                            cryptocurrency, you’re essentially acting as your own bank. Yet, unlike a bank, your money
                            isn’t backed with insurance and if the location where it’s stored gets hacked or the coins
                            get stolen - you’re out of luck. Once they’re stolen, you can’t get the coins back. Check
                            out all options and ensure that the wallet you select provides maximum security. <br> <br>
                        </p>

