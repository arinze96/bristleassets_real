                            Many people assume that cryptocurrency is private and anonymous; however, this is only
                            partially true. While it’s true that leading cryptocurrencies don’t display personal
                            details, enough information is displayed that it may be possible to trace the owner of
                            transactions with enough research. <br> <br>

                            Many users value privacy and, in the future, will be looking for options that are truly
                            private. This provides a positive outlook for Zcash miners, who accumulate coins when the
                            mining reward is still high and hold and sell them when the value increases.
                        </p>

