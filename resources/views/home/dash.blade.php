                            mining, we only require you to enter the hash rate you purchased, and your mining contract
                            fee. <br> <br>

                            Note that even though we provide a good indication of your profits, the volatility of Dash
                            prices alongside the variability of mining difficulty can quickly change how much profits
                            you earn – so check back often, and know that your profitability can change quickly. <br>
                            <br>
                        </p>

                        <h3>
                            Common questions about Dash
                        </h3>

                        <p>
                            Can my raspberry pi be used to mine Dash? <br> <br>

                            Yes, but your raspberry pi is not really powerful enough – it will take forever to mine a
                            Dash coin with it, so we suggest you don’t even try. <br> <br>

                            Can I make a profit mining Dash?<br> <br>

                            Yes, but it really depends on a number of variables – our calculator will be able to tell
                            you, based on your inputs.<br> <br>

                            What are the laws around mining Dash?<br> <br>

                            Most countries do not have any laws governing cryptocurrency mining, but note that you might
                            need to pay taxes on your mining revenue.<br> <br>

                            Can I use my phone to mine Dash?<br> <br>

                            Android or iOS handsets can be used to mine dash, but it would be almost impossible to make
                            a profit and mining Dash with your phone means it will be at risk of overheating.<br> <br>

                            What are the safety concerns around mining Dash?<br> <br>

                            Make sure your hardware is always cool, and ensure you join a mining pool you can trust. If
                            mining using a cloud provider, make sure you choose a provider you can trust too.<br> <br>

                            When will the very last DASH be mined?<br> <br>

                            There is a limited number of Dash coins that can be mined. It looks like the last Dash coin
                            will be mined in 2030.<br> <br>
                        </p>
                        <h3>
                            More information about Dash
                        </h3>

                        <p>
                            Dash was developed with a strong focus on the payments industry, but Dash is nonetheless an
                            open-source, P2P cryptocurrency just like Bitcoin. It can be spent securely both offline and
                            online thanks to the fact that it is a money-centric cryptocurrency – in other words it is
                            portable, easy to acquire and easy to divide into smaller units. On top of all these
                            achievements there is also the fact that the transaction fees for Dash is relatively low.
                            <br> <br>

                            Dash inherited all the key features of Bitcoin, but it’s added in several ways. For example,
                            you can instantly send Dash coins thanks to its InstantSend function, while you can also
                            choose to privately send Dash coins thanks to the PrivateSend function. <br> <br>

                            Other interesting features about Dash includes the fact that Dash is based on a model which
                            implies self-funding, and self-governance. In essence the Dash network can pay people or
                            companies to do work for the network – if that work contributes to the network. <br> <br>

                            You may have heard of a DAO – a decentralised autonomous organisation. The way Dash was
                            designed has meant that Dash is one of the first DAOs. <br> <br>

                            Dash developers say that the maximum number of Dash coins that will ever come into
                            circulation will be 18 million coins. As people mine DASH more and more coins will come into
                            circulation, the expectation is that all 18 million DASH will be in circulation by 2030.
                        </p>
                        <h3>
                            The team behind DASH
                        </h3>

                        <p>
                            Dash is open source, and decentralised. The way it is governed and funded means that it
                            makes it easy to create independent entities which can support the Dash network. Development
                            of Dash is driven by Dash Core Group Inc., the company is in charge of managing the Dash
                            project as well. <br> <br>

                            Dash Core Group have a range of departments which work together to ensue that Dash is
                            supported thoroughly – including devops, management, marketing and business development and
                            a department which supports overall strategy. Dash Core Group has an HR department too as
                            well as a dedicated finance department. A range of senior advisors and external parties
                            contribute too and the network has a number of close associates.
                        </p>

                        <h3>DASH algorithm</h3>

                        <p>
                            The Dash hashing algorithm has a number of advantage and disadvantages. The positive points
                            about the algorithm driving DASH include the fact that it is secure, effective and
                            relatively easy to mine – including low cost of power. <br> <br>

                            On the flipside, the hashing algorithm used for Dash has some disadvantages – we’re not
                            convinced of its longevity and it is somewhat vulnerable to botnet attacks. There is little
                            in the way of a network effects too and we don’t think the community around the hashing
                            algorithm is all that extensive. <br> <br>

                            So, while the X11 algorithm is unique and highly effective the extent to which it is
                            successful will depend on a couple of its essential features, and whether the Dash team can
                            prove the ability to maintain longevity and to ensure the coin remains effective. If so it
                            might illustrate that X11 is a good algorithm for any altcoin. <br> <br>
                        </p>

                        <h3>
                            P-o-W Consensus
                        </h3>

                        <p>Dash uses proof-of-work consensus, but users can earn rewards on top of the proof-of-work
                            system by opting to run a master node, users running master nodes get additional rewards for
                            running this special server. Dash is similar to many other coins because of its
                            proof-of-work mechanism. Each Dash block contains two parts:</p>

                        <ol>
                            <li>
                                A header which includes important parameters such as when the block was created, a
                                reference link to the block prior to it plus a Merkle tree root which covers the
                                transaction block.
                            </li>
                            <br>
                            <li>
                                A block list of all transactions in that block
                            </li>
                        </ol>

                        <h3>
                            DASH forks
                        </h3>

                        <p>
                            Every cryptocurrency has a source code, or protocol that operates it. A fork is essentially
                            a change to that source code – you could see it as a change to the rules that governs a
                            cryptocurrency too. You get two different types of forks:
                        </p>

                        <ol>
                            <li>
                                A soft fork means that the new rule set works just fine with the old rule set
                            </li>
                            <br>
                            <li>
                                In contrast, a hard fork implies what is really a brand new coin – the rules are
                                incompatible
                            </li>
                        </ol>

                        <p>
                            DASH has been forked a lot of times – think for example of Crown, SAFE or PIVX. In fact,
                            Dash isn’t a brand new coin, it was forked from Bitcoin. Dash was forked from Bitcoin
                            because it was intended to fix some problems with Bitcoin. As a result, some of the biggest
                            flaws and issues of Bitcoin doesn’t exist in Dash which means, in particular, that there is
                            a big difference in efficiency. Dash is offers greater anonymity while also performing
                            faster compared to Bitcoin.
                        </p>
