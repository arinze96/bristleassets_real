                      <h3>
                        A few more facts about Aeon
                      </h3>

                      <p>
                        Aeon is a fork of Monero and was launched in 2014. The Aeon fork of Monero has no instamine nor a premine, there is no developer tax and Aeon is fully funded by community contributions. In 2018 the development fund for Aeon was up to USD 1m. The protocol that supports Aeon is CryptoNote, and it uses a CryptoNight Lite algorithm. <br> <br>

                        This brings a number of essential features. Aeon is anonymous, the way it works protects its user’s data too. You cannot trace payments on Aeon thanks to its ring signature and transactions can’t be linked either. The Aeon blockchain is resistant to analysis and resistant to ASIC mining – in other words, you can use a CPU or GPU to mine Aeon. <br> <br>
                        
                        Aeon has a maximum supply of 18.4m coins and is still actively under development – in fact, the coin’s roadmap is also in flux. <br> <br>
                      </p>

                      <h3>The team behind Aeon</h3>

                      <p>
                        Aeon was launched in 2014 on Bitcoin Forum by a developer who decided to stay anonymous. However, towards the end of the year it looked like Aeon had been abandoned as the original developer was no longer working on it. That said by 2015 a new developer took over – his username is Smooth. <br> <br>

                        This handover was with the approval of the Aeon community, and Smooth continued to work on Aeon alongside his work on the Monero project. At the time the Aeon project was rebranded and the team announced a new roadmap for Aeon. <br> <br>
                        
                        In April 2015 Smooth became the lead developer on Aeon – Smooth is, to this day, one of the most knowledgably people in the cryptocurrency sphere. Smooth is still continuing his work for Monero. <br> <br>
                      </p>

                      <h3>
                        The hashing algorithm behind Aeon
                      </h3>

                      <p>
                        CryptoNote is the protocol behind Aeon, the coin uses CryptoNight-Lite as its algorithm – and this means that you can use the CryptoNight all-in-one miner. Bitmain released a CryptoNight ASIC miner, after that many CryptoNote coins developed forks to change the way their Proof-of-Work consensus works. Aeon, for example, adopted the Lite V7 algorithm.
                      </p>

