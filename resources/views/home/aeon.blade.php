                      <h3>
                        Why Aeon mining is profitable
                      </h3>

                      <p>
                        Like we said, Aeon is a fork of Monero and the reason Aeon was created was to make a cryptocurrency which is mobile friendly – a coin that can be run on laptops, mobile devices and personal computers. The principle of Aeon lies in a proof-of-work mechanism which allows more efficient (faster) verification of transactions on the Aeon blockchain, compared to Monero. <br> <br>


                        Aeon achieves this by reducing the amount of data which is kept on the blockchain – this also leads to improved levels of anonymity, while it is also possible to mine Aeon on a computer that does not have much storage. <br> <br>
                        
                        There are other differences between Aeon and, say, Bitcoin. Aeon is based on a algorithm called CryptoNote, this algorithm is different because it enforces a higher level of verification – which also means that there are more blocks that are orphaned. This lighter proof-of-work mechanism means much better performance on the device, by a factor of ten or more. <br> <br>
                        
                        Also, Aeon payments are by default sent anonymously. However, if you prefer a transfer that can be traced, you can accomplish this. There is a quick and fee-free transfer option for payments which are not sensitive – it is called an exchange transfer. So, Aeon uses the technical benefits of Monero but there is a unique twist to Aeon – the creators of Aeon is a distinct community with specific goals. It may well be that Aeon is the perfect coin for you to mine. <br> <br>
                      </p>

                      <h3>
                        Optimal Aeon mining
                      </h3>

                      <p>
                        You don’t need an ASIC mining rig to mine Aeon – you can use either a CPU or a GPU-based miner, and both Nvidia and AMD GPUs are supported. You have plenty of options when it comes to mining – think about a Claymore miner, an SRB miner or perhaps something from XMRig. However we think your best option is XMR-Stak. The XMR-Stak software is available on Github, you just need to set it up correctly to mine Aeon efficiently. <br> <br>

                        Interestingly, XMR-Stak allows you to mine using both the CPU and the GPU in your computer. Furthermore, it comes with an easy user interface while it also optimises to achieve the best hashrates. You also get detailed statistics while it also supports connecting to multiple mining pools. Note that XMR-Stack is actively being developed, we expect a lot of improvements to come in the near future.
                      </p>

