                          </li>
                          <br>
                          <li>
                            Get a wallet that can store your Aeon coins
                          </li>
                      </ul>

                      <h3>
                        Choosing your Aeon mining hardware
                      </h3>

                      <p>
                        First, from a software perspective, we suggest you jump in by using XMR-Stack because it gives you a lot of flexibility – you can use either a CPU or a GPU, and when it comes to GPUs you have a choice between AMD and Nvidia too. There are plenty of CPUs you can consider, but we would recommend you look at one of the high-end AMD or Intel chips. <br> <br>

                        Always compare all the important factors of a miner before you jump in and buy – think about the price of the GPU, the hash rate as well as the overall power consumption
                      </p>

                      <h3>
                        Other points to think about before you start mining Aeon
                      </h3>

                      <p>
                        Crypto mining rigs use a lot of power and also generate heat, so you need to make sure your rig is kept cool to make sure it performs day in, day out – cooling can make the difference between losing the money you invested in your rig, or making a ongoing profit. <br> <br>

                        Noise is another factor you should consider. Cooling your rig will lead to fan noise, so your mining equipment needs to be in a location where noise is not a problem. Plan accordingly, and read expert advice on how to deal with the noise created by a mining rig.<br> <br>
                        
                        You also need to choose whether you prefer to mine Aeon on your own as an individual (solo mining) or prefer to mine Aeon as part of a mining pool. We think joining a pool of Aeon miners is better for profitability, mining in a pool means more regular pay-outs so you get a steady flow of revenue instead of occasional large lumps. There is a mining fee involved, but the degree of revenue certainty – you would usually get a daily payment – means that mining pools are often a better option.<br> <br>
                        
                        You have plenty of options when it comes to joining a mining pool, we suggest you consider both the fee of the pool, where its servers are located as this affects latency and, of course, the mining pool’s reward system.
                      </p>

                      <h3>
                        Configuring your mining hardware and software
                      </h3>

                      <p>
                        Our guide below will explain everything you need to know to get up and running mining Aeon on a Windows computer. The instructions are for XMR-Stak, in addition to XMR-Stak you will need a wallet that can accept Aeon coins as well as a Windows PC with an internet connection. We’ll also explain how you can connect to the Aeon mining pool of your choice. One thing to consider: we suggest that you never use the wallet address of an Aeon exchange, instead use a dedicated Aeon wallet.
                      </p>

                      <ol>
                          <li>
                            Determine your wallet address and put it in the clipboard <br> <br>

                            Find out what your wallet address is and put the address into your clipboard so that you can paste it into the mining app.
                          </li>

                          <br>

                          <li>
                            Download and run XMR-Stack <br> <br>

                            XMR-Stack is located on Github. Find the file xmr-stak-win64.zip where you saved it, extract it, and navigate to the folder it’s extracted into. Next, double-click on xmr-stak.exe to start the mining app. You might get a warning for Windows or anti-virus software, just tap “yes” or “allow to run” when prompted.
                          </li>
                          <br>

                          <li>
                            Instruct XMR-Stak to start mining Aeon <br> <br>

                            Once XMR-Stack is open, type “aeon7” and press enter to get started.
                          </li>
                          <br>

                          <li>
                            Connect XMR-Stack to the mining pool <br> <br>

                            Enter “mine.aeon-pool.com:5555” and press Enter, this will configure Aeon to use that mining pool – so that your PC can contribute to the mining pool
                          </li>
                          <br>

                          <li>
                            Add your wallet address <br> <br>

                            Your wallet address should still be in the clipboard. To paste it, click the program icon which sits in the left, upper corner of the XMR-Stack window – access Menu, select the Edit submenu and then click Paste. Press Enter. If your wallet address is no longer in your clipboard, simply minimise the XMR-Stack window and copy and paste your wallet address.
                          </li>
                           <br>
                           <li>
                            Password and questions <br> <br>

                            When prompted for a password, leave it blank, and make sure you answer “no” to all the other prompts. A password is not required to view your mining stats, and your mining proceeds will be deposited into your wallet automatically. Assuming you did everything we explained above correctly your XMR-Stak app will now be connected to the mining pool and will be mining actively. <br> <br>
                            
                            During mining you can type “c” for the connection status, “h” to see your hashrates and “r” to see a table of results. <br> <br>
                           </li>
                      </ol>

                      <h3>
                        Mining Aeon in the cloud
                      </h3>

                      <p>
                        Setting up XMR-Stack and paying for the hardware to run it can be too high a hurdle for many of us. An alternative is cloud mining where you rent the computing power to mine Aeon – none of the electricity bills or hardware costs will be your responsibility. You sign a contract with a provider and in turn “rent” the mining resources. <br> <br>

                        There are a lot of advantages to mining in the cloud: you don’t need to buy the hardware, results come in immediately and you don’t need to worry about noise or heat. Finally, you can usually get your payments in a cryptocurrency of your choice – it needn’t be in Aeon coins.
                      </p>

