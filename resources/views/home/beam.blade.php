                    <div class="tradingview-widget-container" style="margin:auto;">
                        <p>
                            Beam is one of the most interesting coins to make its appearance in 2019. The mainnet for
                            Beam was launched on the 15th of January, with the person behind Litecoin saying that Beam
                            has the potential to become as important as Bitcoin. At the end of 2019 Beam is still
                            around, so let’s check out how you can mine Beam.
                        </p>

                        <h3>
                            Understanding Beam
                        </h3>

                        <p>
                            Just like Grin, Beam makes use of a cryptocurrency privacy protocol that’s called
                            Mimblewimble. For this reason crypto experts often talk about Beam and Grin in the same
                            breath. The reason why Mimblewimble is so important is because it can dump a large
                            proportion of old transaction data that’s recorded to the blockchain. <br> <br>

                            The result is that blockchains remain relatively “light” to handle, without enormous extra
                            amounts of data – blockchain users do not need to spend days and days downloading the
                            blockchain. At the same time it also improves the privacy of the blockchain. <br> <br>

                            Beam has other advantages too, according to its creators. First, the supply of Beam coins is
                            capped – there is a maximum number of Beam coins that can be mined. The creators also think
                            it is very user friendly, and people often mention that ordinary investors were not asked
                            for funds during launch – there was no ICO.
                        </p>

                        <h3>
                            Features of the Beam coin
                        </h3>

                        <p>
                            The website for the Beam project points to a number of key factors that makes Beam special
                            compared to similar coins.
                        </p>

                        <h3>
                            Privacy features
                        </h3>

                        <p>
                            To start off with, all the transactions on the Beam network are anonymous because
                            transaction information is not stored on the Beam blockchain. Furthermore, auditing is
                            “opt-in” – in other words, only specific auditors are able to retrieve a list of the
                            transactions on the blockchain alongside the associated documentation. So, regulatory
                            requirements can be satisfied too.
                        </p>

                        <h3>
                            Scalability and sustainability
                        </h3>

                        <p>
                            Use of the Mimblewimble protocol means that Beam is relatively light compared to other
                            blockchains and that it can store a lot of transactions over a long period of time. Beam was
                            open source from the start with heavy involvement from the community – including the Beam
                            Growth Pool. This pool takes 20% of total mining rewards, which is given to developers to
                            pay them for the promotion and development of Beam.
                        </p>

                        <h3>
                            Usability
                        </h3>

                        <p>
                            You can mine Beam using most GPUs, unlike some other coins which would only support ASIC
                            mining. Furthermore, because of Beam’s scriptless script tech it means that many
                            transactions can be completed on Beam beyond a simple transfer of coins. You can do
                            time-locked transactions, use Beam for escrow and also perform atomic swapping.
                        </p>

                        <h3>
                            So is Beam a coin worth checking out?
                        </h3>

                        <p>
                            There were two very popular new coins in 2019: Beam, and of course Grin. Beam is popular
                            because they launched in the middle of a cryptocurrency bear market, and they didn’t use an
                            initial coin offering to do so. Instead, they launched a unique coin based on Mimblewimble
                            and the coin is already making solid progress. <br> <br>

                            Beam’s developers had other options, options often used by less enterprising blockchain
                            developers. Instead of using a copycat fork to retrieve money from existing users Beam
                            figured they would make a brand new coin, and their efforts deserve applause.
                        </p>

                        <h3>
                            How did Beam fare in 2019
                        </h3>

                        <p>
                            Like we said, Beam is making good progress. The maximum supply of Beam coins is 262.8m, and
                            by the end of 2019 51m Beam has entered the supply. The overall market cap for Beam is USD
                            27.6m, while the daily volume is just over USD 19m. Beam was seen trading at USD 0.53 by the
                            end of 2019. <br> <br>

                            Keep in mind that the peak value of Beam was USD 3.18 in January 2019, it got nearly close
                            to the same value at the end of June 2019. It’s a long way away from the USD 7,000+ of
                            Bitcoin, but it does not mean that mining Beam is not profitable. <br> <br>

                            Beam is widely available – you can buy Beam from a range of popular cryptocurrency markets
                            including Hotbit, BitForex and BKEX. Note however that the volume of Beam on some platforms
                            is very low.
                        </p>

                        <h3>
                            Beam wallets
                        </h3>

                        <p>
                            Beam’s developers created a custom wallet for Beam – it looks great on an iPhone, and the
                            iPhone app works a lot like every other app you’d use – using your fingerprint for
                            authentication. In fact, it looks a lot like a budgeting or banking app. <br> <br>

                            There are Beam apps for a range of other platforms too – Android, where you can download the
                            apk file or choose the app in Google Play; and of course desktop wallets for Windows, Linux
                            and OS X.

                        </p>

                        <h3>Mining Beam</h3>

                        <p>
                            The algorithm behind Beam is based on proof of work, but is a modified version – like
                            Equihash 150.5. Beam’s mining algorithm is called Beamhash. New blocks are created around
                            every minute and the size of a blog is about 1,024 KB – containing about a thousand
                            transactions. Beam’s blockchain operates on an encrypted network so mining is completed
                            using a SSL connection. <br> <br>

                            We said earlier that the supply of Beam is capped – this means that the block rewards are
                            reduced over time. In year one, the block reward was 80 coins for each Beam block that is
                            mined, this drops to 40 for year 2 to 5. In year 6 it becomes 25 coins per block, and
                            thereafter every four years until year 129 the number of coins awarded are decreased by 50%.
                            In year 133 there will be no more Beam coins awarded for new blocks. <br> <br>

                            In theory Beam should be ASIC resistant for about a year, the developers made use of hard
                            forks to ensure that this is the case – one in August 2019, and another about the same time
                            in 2020.
                        </p>

                        <h3>
                            Mining Beam with a GPU
                        </h3>

                        <p>
                            As the Beam blockchain is currently resistant against ASICs you can still mine Beam using
                            either an Nvidia or an AMD GPU. You need at least 4GB of graphics RAM, but a GTX 1060 from
                            NVidia can mine Beam with just 3GB. <br> <br>

                            In terms of expected hashrates, you can expect a GTX 1060 to achieve 14 Sol/sec, while a
                            top-end RTX 2080 can achieve 41.5 Sol/sec. For AMD kit, expect 10 Sol/sec on a RX 480 or
                            570, and 24 Sol/sec on a Radeon VII with 16GB. <br> <br>

                            Setting up your GPU for mining Beam is easy, and we recommend that you use Gminer for both
                            Nvidia and AMD GPUs. You can download it here, use the password “2miners” to access the file
                            archive. You need to use a specific batch file for Gminer – one that will be compatible with
                            the 2Miners pool will contain: <br> <br>

                            miner.exe --algo beamhash --server beam.2miners.com --port 5252 --ssl 1 --user
                            MY_ADDRESS.RIG_ID --pass x <br> <br>

                            Where MY_ADDRESS will be your Beam wallet address, and RIG_ID will be your rig name.
                        </p>

                        <h3>
                            Cloud mining for Beam
                        </h3>

                        <p>
                            Beam can be mined without using your own mining equipment – instead, services like The
                            Miners can be used to mine Beam as this allows you to rent the computing power required to
                            mine Beam.
                        </p>

                        <h3>
                            How profitable is it to mine Beam?
                        </h3>

                        <p>
                            Mining profitability will always vary with Beam – and depends on what mining equipment you
                            have available, what the current value of Beam is as well as the cost of electricity in your
                            area. You can make these calculations yourself, or use 3rd parties to work out how Beam
                            relates to other cryptocurrency for mining profitability. <br> <br>

                            At the time of writing, Beam is the second most profitable coin to mine, with average block
                            awards taking about 9 hours. Also keep in mind that, for a while at least, Beam cannot be
                            mined with ASICs so you don’t need these specialist mining rigs which can also rapidly lose
                            their value – you can mine Beam using a GPU.
                        </p>
