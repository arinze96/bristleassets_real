                        <h3>
                            Why mining Monero is a good idea
                        </h3>

                        <p>
                            Of course, everyone who mines a cryptocurrency is doing it because they want to make a
                            profit – very few people mine cryptocurrencies for any other reason. Choosing the right
                            cryptocurrency to mine is important, and for a lot of miners privacy is a key aspect of the
                            ideal cryptocurrency.
                        </p>

                        <p>
                            Bitcoin is not known as the most privacy-focused of coins, but Monero is different – Monero
                            has a range of privacy features built-in ensuring more private transactions. For example, if
                            someone sends you Monero coins you will not be able to check what the source of the coin
                            transfer is.
