                        <h3>
                            Other things you need to think about before mining XMR
                        </h3>

                        <p>
                            Keep in mind that a mining rig can cause other issues too. Watch out for high electricity
                            bills to start off with, and also keep in mind a mining rig will make noise and generate
                            heat. This is the case for GPU-based XMR mining rigs too.
                        </p>

                        <p>
                            Another thing you should be careful about is the latency of your internet connection,
                            because if you and another miner both find a block at the same time it will be the miner
                            that responds faster that gets the reward. So, if your internet connection latency is high
                            you run the risk of missing out on block rewards and it could mean that you’ve spent hours
                            mining for no reason. In addition to low latency you also need at least 500MB of data
                            capacity per device, every day.
                        </p>

                        <h3>
                            Setting up for XMR mining
                        </h3>

                        <p>
                            In this section we will explain how you should set up your Monero mining rig.
                        </p>

                        <ol>
                            <li>
                                Download the Monero mining software <br>

                                You need a file called xmr-stack, which is available from a Github release page. You can
                                choose from two different versions, one is from a user called psychocrypt, the other
                                from a user called fireice-uk. However, you should always download the latest version.
                            </li>
                            <br>
                            <li>
                                Installing your mining software <br>

                                It’s easy really, all you need to do is to first download the .zip file saving it
                                somewhere you’ll remember. You can extract the zip file where it suits you, but keep in
                                mind that your AV package might block it as a threat. It is normal, and you can bypass
                                this threat from your virus software. Ensure you add an exclusion for the mining
                                software, so your AV package doesn’t keep bothering you.

                            </li>
                            <br>
                            <li>
                                Find xmr-stak.exe <br>

                                When you’ve extracted all the files you need to look for one specific file, which sits
                                alongside a total of four other files. The file you want is xmr-stak.exe
                            </li>
                            <br>

                            <li>
                                Sign up to a mining pool <br>

                                Like we explained earlier, mining in a pool is easier and you can get paid more
                                regularly. However, you need to join a pool first which will give you the benefit of a
                                regular payout. Note that mining pools charge a small fee for their service. It’s worth
                                doing your own research to find the best mining pools with the lowest fees.

                            </li>
                            <br>
                            <li>
                                Configure the mining software <br>

                                You need to configure xmr-stak.exe, but it’s simple really – just run xmr-stak.exe which
                                is the configuration wizard. It’ll ask you some questions, ensure you complete these
                                carefully. One of these is a password section, and you will also be asked for an
                                identifier of the rig you plan on using. <br>

                                Furthermore, you need to enter the port you want to utilise – TLS or SSL and make sure
                                you tap “n” for “no” when presented with Nicehash, you don’t want to use Nicehash. Also
                                decline the use of multiple pools with a “no”. <br>

                                When you’re done the mining software will automatically try to connect to the pool you
                                are using and you can start to mine XMR. Note that, during setup, xmr-stak.exe will
                                create three more files for configuration. This includes pools.txt, nvidia.txt and
                                cpu.txt.

                            </li>
                            <br>

                            <li>
                                Starting up the Monero miner <br>

                                You can make a shortcut to the miner so it starts up automatically. Just drag and drop a
                                shortcut to xmr-stak.exe. This will auto-start the miner as Windows boots. It is easy to
                                get to the startup folder in Windows 10, tap Start and then Run and type in
                                shell:startup.
                            </li>
                        </ol>

                        <h3>
                            Considering mining Monero using a cloud mining pool
                        </h3>

                        <p>
                            Want to start mining straight away without buying anything in the way of physical equipment,
                            paying an energy bill or worrying about keeping your rig cool and ventilated? Cloud mining
                            might be the best option for you.
                        </p>

                        <p>
                            In terms of advantages cloud mining means that you do not need to pay electricity costs and
                            you don’t need to order hardware (which might take a while to arrive anyway). There’s also
                            no need to deal with high levels of heat and fan noise isn’t an issue.
                        </p>

                        <p>
                            On the flipside, cloud mining does have disadvantages, including the fact that you will earn
                            less (the cloud mining vendor needs to cover its costs) alongside the risk of getting
                            scammed or defrauded by a cloud mining vendor. Also note that nothing prevents a cloud
                            mining pool from stopping payouts, or completely stopping all operations, if profits drip
                            below an acceptable level: it could be written into your mining pool contract. Finally,
                            you’ll never own the mining equipment.
                        </p>

                        <p>
                            Some of these disadvantages can be mitigated if you make the right choice of cloud mining
                            vendor. So, you need to choose a provider alongside the mining algorithm as well as the
                            amount of hashing power that you require. Watch out for the provider’s reputation, how long
                            your contract is for, the algorithm in use and of course the total hashing power.
                        </p>

