                        <h3>
                            A brief introduction to Monero
                        </h3>

                        <p>
                            XMR, or Monero, is an open-source cryptocurrency which is mainly focused on providing
                            additional privacy, over and above the typical decentralisation goals of cryptocurrencies.
                            Though the creators of Monero had good intentions some issues have surfaced: for example,
                            hackers have targeted XMR because they wanted to try and crack the privacy abilities of
                            Monero.
                        </p>

                        <p>
                            In a unique twist to Monero, every XMR coin can be replaced by another XMR coin which means
                            that Monero has a real advantage compared to other cryptocurrencies such as BTC.
                        </p>

                        <p>
                            Over time Monero’s developers have made a range of improvements to XMR, including changing
                            the database structure which supports the XMR blockchain – it’s made the blockchain more
                            flexible and more efficient.
                        </p>

                        <p>
                            Another step in Monero’s development was establishing a minimum size for ring signatures
                            which meant that every Monero transaction is private – by mandate. RingCT, another
                            improvement, means that the actual transaction amount is also hidden. It’s all in the
                            interest to make Monero more secure and more private.
                        </p>

                        <p>
                            Monero is still making efforts to try and ensure that it’s initial goals of ensuring
                            transaction privacy and transaction confidentiality remains at the centre of the coin, while
                            blockchain efficiency and overall ease of use takes a back seat.
                        </p>

                        <h3>
                            The team behind XMR
                        </h3>

                        <p>
                            Monero is completely open source, nobody owns the code – and no organisation has central
                            control over XMR. No company owns or controls Monero either. The developers who are
                            interested in Monero tend to give their contributions for free. These contributions or
                            suggestions can be funded via a system where the community can support specific ideas that
                            need funds to proceed – it’s called FFS.
                        </p>

                        <h3>
                            Understanding the Monero hashing algorithm
                        </h3>

                        <p>
                            CryptoNight is the hashing algorithm which enables users to mine Monero. There are numerous
                            features of CryptoNight that are worth thinking about. Most importantly the speed between
                            the processor and the memory modules in use is important with this hashing algorithm because
                            it is highly memory bound
                        </p>

                        <p>
                            As a result, GPU mining power has less of an advantage over CPU mining power in the case of
                            CryptoNight, compared to other mining algorithms. This was seen as an advantage. ASIC miners
                            pose a different threat altogether, and that is why Monero has moved XRM to the latest
                            version of CryptoNight – version 7.0.
                        </p>

                        <h3>
                            What is an XMR fork?
                        </h3>

                        <p>
                            Monero goes through an upgrade every six months to upgrade its protocol. Each hard fork
                            introduces new features to Monero – for example, the ability to conduct transactions which
                            are bigger and more resource intensive while still remaining private. Other examples include
                            the support for Ledger Nano S as well as the ability to make transfers that are
                            multi-signature.
                        </p>

                        <p>
                            Interestingly, in the battle against ASICs, Monero’s last fork made a change to the
                            CryptoNight algorithm which meant that every existing ASIC rig was made useless from that
                            day onwards. Chinese company Bitmain’s Antiminer X3 was so powerful that Monero felt it
                            needed to act.
                        </p>

                        <p>
                            The person who create Monero, Riccardo Spagni, said that the change in the way the
                            cryptocurrency functions was intended to ensure that it will be more resistant to attempts
                            to mine it via ASICs. It is the extensions used by Monero which boosts the effectiveness of
                            CPU mining for Monero, so there is a planned unfair advantage for CPUs. Spagni will be less
                            against ASICs if there was an extension that could be implemented widely to ensure that ASIC
                            mining hardware is also relatively democratic, rather than distorting mining abilities.
                        </p>
