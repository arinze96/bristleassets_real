                <div class="col-md-12 col-sm-12 currency-heading">
                    <h2>Ethereum Mining </h2>
                </div>
                <div class="container">
                    <div class="tradingview-widget-container" style="margin:auto;">
                        <p>
                            Lots of people have jumped in and used their computer resources or purchased hardware so
                            that they can mine Ethereum (ETH). If you’re thinking of starting an Ethereum mining
                            operation read this article to learn all you need to know about mining ETH and about the
                            different options you have for ETH mining software.
                        </p>

                        <h3>
                            Ethereum: the basics
                        </h3>
                        <p>
                            Mining for cryptocurrencies has changed a lot over the years, and that’s the case for
                            Ethereum too. It used to be techies who mined cryptocurrency - in part because they had the
                            skill to easily do so - but also because technically minded people have an interest in
                            difficult to use software, like cryptocurrency mining apps. However, cryptocurrency mining
                            software is now much easier to use so it is very possible for the average person to mine
                            cryptocurrency.
                        </p>

                        <p>
                            Even the most complex CLI (command line interface) software for mining has now become much
                            easier to use, while there are also plenty of GUI (graphical user interface) options that
                            makes mining ETH a matter of pointing and clicking.
                        </p>

                        <h3>
                            Mining ETH is just like mining BTC
                        </h3>

                        <p>
                            Bitcoin (BTC) and Ethereum works in similar ways and as a result mining ETH and BTC is quite
                            similar too. The goal is to make sure the currency is decentralised. As a result
                            cryptocurrency networks require a large network of computers to process transactions.
                        </p>

                        <p>
                            Mining is the process by which computers validate the transactions of cryptocurrency users
                            on the cryptocurrency blockchain. It is done using a complex cryptography algorithm and the
                            process is called proof of work.
                        </p>

                        <p>
                            Anyone can contribute computing power to enable the validation of transactions and you get a
                            reward for doing so. You get a couple of ETH coins for the block you validate and there is
                            another reward too – something called “gas” which is similar to a transaction fee.
                        </p>

                        <h3>
                            Mining difficulty grows and ASICs becomes popular
                        </h3>

                        <p>
                            However over time it becomes more difficult to solve these blocks. This difficulty is
                            measured by the “hash rate”. Though, as the “hash rate” for ETH has gone up, the price of
                            ETH has also gone up over the years. However, a big drop in the value of Ethereum in 2018
                            did not see an equal decline in “hash rate” – instead, the hash rate for Ethereum remained
                            high.
                        </p>

                        <p>
                            Why did the ETH hash rate stay high? In part because more and more people tried solving for
                            transaction blocks and in part because the mining hardware used by people (or indeed large
                            groups of people) became more powerful and able to solve blocks much more quickly. For
                            example, application-specific integrated cir
                            cuits entered the mining scene. These ASICs made for much more powerful mining machines.
                        </p>

                        <p>
                            In fact, ASICs are so powerful and relatively hard to buy that many people think the mining
                            power that’s increasingly concentrated in the hands of a small group of ASIC owners has
                            meant that ETH is becoming less decentralised than it used to be. There has been some
                            questions around what can be done to prevent concentration of power over the network in the
                            hands of a few groups of powerful miners – but the necessary changes to the ETH proof of
                            work mechanism has not yet been made.
                        </p>

                        <p>
                            You can still mine ETH without the use of specialist ASICs, but you will need a powerful
                            GPU. In other words, a solid gaming card from Nvidia or AMD. You also need an up to date
                            computer to run the mining software.
                        </p>

                        <h3>
                            Why mining ETH is worth the hassle
                        </h3>

                        <p>
                            It’s clearly no longer all that easy to mine ETH and you have other cryptocurrencies you can
                            investigate too. Besides, you could always buy ETH from an exchange, right? However mining
                            Ethereum has its benefits. For starters, you won’t take much of a risk if you only mine a
                            little bit of ETH as a hobby – you just use your existing GPU. It’s an easy way to join the
                            cryptocurrency craze and make some money.
                        </p>

                        <p>
                            However, mining ETH has a deeper meaning – by mining you indicate that you support the
                            values behind ETH as your mining activity helps ETH stay stable and ensures that it remains
                            decentralised. Besides, the profits you make from a little bit of hobby ETH mining could
                            easily pay for more serious mining equipment – and the opportunity to mine significant
                            amounts of ETH.
                        </p>

                        <h3>
                            The basics of Ethereum mining
                        </h3>

                        <p>
                            Like we said, you no longer require a computer engineering degree to start mining ETH. It’s
                            really simple nowadays. You need to take care of three important aspects.
                        </p>

                        <h3>
                            Getting a mining rig
                        </h3>

                        <p>
                            The hardware you pick to mine ETH is really what determines how much ETH you will mine, and
                            how much profit you will make. You start off with a basic computer which is really just like
                            any other PC you would use. You need a CPU, ample memory, cooling and a power supply (PSU)
                            that has enough power to drive your GPUs.
                        </p>

                        <p>
                            You will need a GPU (or indeed an ASIC board) if you want to mine any significant amount of
                            ETH. Consider something like an Nvidia GTX 1070ti or above, high-end GPUs from AMD will also
                            work. Always update your GPU firmware and drivers and try and buy a PSU that is efficient:
                            an efficient PSU can reduce your mining electricity bill and could help you get a bit more
                            speed out of your GPU.
                        </p>

                        <p>
                            All-in-all you want the most powerful hardware you can afford to achieve high hash rates,
                            but at the same time achieve these high hash rates using the minimum amount of electricity.
                            Eventually you could conclude that a PC + GPU will only take you so far, and that you should
                            really buy a separate, dedicated mining rig.
                        </p>

                        <h3>
                            Configure an ETH wallet and mining software
                        </h3>

                        <p>
                            There are plenty of ways to set up a crypto wallet, you might even find that your mining
                            software includes a wallet. If you want to keep your newly minted ETH as safe as possible
                            you should really use your own wallet. MEW or MyEtherWallet is a good choice and it’s free
                            to use, all you need is a standard web browser.
                        </p>

                        <p>
                            If you’re a beginner you might want to choose a GUI mining software package that makes it
                            easy to mine ETH. Note however that GUI mining software is generally not as versatile as the
                            CLI equivalent, but for many people typing commands into a CLI can be too difficult when
                            starting.
                        </p>

                        <h3>
                            Recommended ETH mining software
                        </h3>

                        <p>
                            Choosing between CLI and GUI should really be based on what you think your technical
                            abilities are. If you are comfortable with Linux-style command line inputs you’ll be just
                            fine with a CLI tool and you’ll enjoy the advanced functionality these tools bring. On the
                            other hand if you’re just starting with mining try the GUI tool we recommend.
                        </p>

                        <h3>
                            Your command line (CLI) choices
                        </h3>
                        <p>
                            In terms of CLI tools we recommend either Claymore’s Dual Ethereum Miner or EthMiner. You
                            could also try PhoenixMiner. However all three these tools will require you to create your
                            own config files so you need to be comfortable doing that.
                        </p>
                        <br>

                        <ul>
                            <li>
                                Claymore. Many people consider Claymore to be the best ETH mining tool out there, in
                                part because it delivers high profits while it also offers a lot of control over your
                                hardware. You
                                can also mine different coins with Claymore, not just ETH.
                            </li>
                            <br>
                            <li>
                                EthMiner. It’s similar to Geth really and makes use of simple code that uses little
                                resources. You can mine any coin that is based on ETHash including for example ETH
                                Classic. It’s trickier to get going compared to Claymore, however.
                            </li>
                            <br>
                            <li>
                                PhoenixMiner. Claymore is popular but some people say that PhoenixMiner is really more
                                stable and that it is overall a more efficient way to mine ETH compared to Claymore.
                            </li>
                        </ul>
                        <br>

                        <p>
                            CLI tools can be difficult to use but you can always consult the broad communities
                            supporting these tools – but try and pick your tool of choice according to the level of
                            confidence you have in working with CLI alongside your desire to muck around with technical
                            software.
                        </p>

                        <h3>
                            The graphics user interface (GUI) alternative
                        </h3>

                        <p>
                            Cudo Miner is possibly the most popular GUI tool for mining ETH. It’s easy to use because
                            you don’t need to know command line commands or the ability to write configuration files,
                            it’s all point and click.
                        </p>

                        <p>
                            It lets you mine a range of cryptocurrency including ETH and it can even switch mining
                            efforts dynamically so that you are always mining the most profitable cryptocurrency.
                            Interestingly, with Cudo Miner, you always receive your payment in ETH even if you set Cudo
                            to automatically pick which cryptocurrency it mines.
                        </p>

                        <p>
                            Installing Cudo Miner is easy, it works on anything from Windows to Linux through to OS X.
                            In fact, there is very little you need to get going with Cudo Miner as you can start mining
                            with Cudo even before you have a wallet address. All-in-all Cudo Miner can help you find
                            your feet in the world of ETH mining.
                        </p>

                        <h3>
                            Joining a mining pool
                        </h3>

                        <p>
                            Mining ETH on your own can be extremely difficult – it will take a single GPU what seems
                            like forever to mine a single block. Proof of work nowadays require a lot of computing power
                            and the best way to mine is to join a mining pool where users join in the effort to solve
                            blocks – resulting in quicker rewards.
                        </p>

                        <p>
                            So, instead of waiting for a random process to eventually reward you it’s better to link up
                            with other miners in a mining pool to boost your chances. In fact, if you contribute enough
                            computing power to personally push a high hash rate you could look forward to regular
                            payouts from ETH mining.
                        </p>

                        <p>
                            You need to configure your mining software to work with a mining pool but you do get some
                            software solutions which can mine ETH while also linking up with a pool. One of these is
                            Nanpool.org but you can also link a CLI tool like Claymore with Nanpool.org.
                        </p>

                        <h3>
                            It’s easy to get started
                        </h3>

                        <p>
                            There used to be a time when mining a cryptocurrency like ETH was a very technical affair. But over the years ETH mining and mining cryptocurrency in the broad has become so much more accessible. It’s hardly any trouble to set up a mining rig, software and your wallet address. Why not try it with your existing GPU to see how it goes?
                        </p>
